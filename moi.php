      <h2 class="txtcenter"> C'est quoi l'institution saintaspais ?</h2>
      <br>
      <pre class="txtcenter">

L'institution Saint-Aspais est un ensemble scolaire qui propose des formations diverses et variées du lycée à l'enseignement supérieur.

Dans différents domaines comme l'esthétique, le bien-être, le commerce, la gestion, l'informatique, etc. 

C'est un établissement privé sous contrat avec l'État, qui dépend du diocèse de Meaux. Il est situé à Melun, en Île-de-France, et accueille près de 1900 élèves, étudiants et alternants.

L'institution Saint-Aspais a été fondée en 1883 par le chanoine Thomelin, curé de la paroisse Saint-Aspais, qui voulait créer une école pour les jeunes filles pauvres de la ville. 

Depuis, l'établissement a évolué et s'est adapté aux besoins de la société et aux attentes des jeunes. Il a notamment créé un campus d'enseignement supérieur en 2018, qui offre des formations du Bac+2 au Bac+5 en cycle initial ou en alternance.
        
L'institution Saint-Aspais se veut être un lieu d'éducation, de formation, d'accompagnement et d'ouverture, qui respecte les valeurs chrétiennes et humaines. 

Elle vise à former des citoyens responsables, autonomes, solidaires et créatifs, capables de s'insérer dans le monde professionnel et de contribuer au bien commun. </pre>
        

